--configurations table to store customer data used in qlikview
SPOOL nikira-qlikview-ddl.log
SET FEEDBACK off;
set serveroutput off ;

CREATE TABLE CUST_DATA_CONFIG
(
	ID NUMBER(20) NOT NULL,
	CONFIG_KEY VARCHAR(256) NOT NULL,
	VALUE NUMBER(20,6),
	DATA_CYCLE VARCHAR(1),
	CONSTRAINT PK_CDC_ID PRIMARY KEY (ID),
	CONSTRAINT UK_CDC_KEY UNIQUE (CONFIG_KEY)
);
CREATE SEQUENCE CUST_DATA_CONFIG_SEQ INCREMENT BY 1 START WITH 1 NOMAXVALUE MINVALUE 1 NOCYCLE ;
SPOOL OFF
