CREATE OR REPLACE FUNCTION GET_DATE_DIFF (DATE_FIELD DATE, DAYS NUMBER) RETURN DATE IS
DATE_DIFF    NUMBER := 0 ;
ACUTAL_YEAR NUMBER := 0 ;
BEGIN
  DATE_DIFF := TO_CHAR(DATE_FIELD, 'ddd') - DAYS ;
  IF DATE_DIFF < 0 THEN
        SELECT TO_NUMBER(TO_CHAR(DATE_FIELD, 'YYYY')) - 1 INTO ACUTAL_YEAR FROM DUAL ;
    ELSE
    SELECT TO_NUMBER(TO_CHAR(DATE_FIELD, 'YYYY')) INTO ACUTAL_YEAR FROM DUAL ;
    END IF ;
  RETURN TO_DATE(TO_CHAR(LPAD(DAYS||ACUTAL_YEAR, 7, '0')), 'DDDYYYY') ;
END GET_DATE_DIFF ;
