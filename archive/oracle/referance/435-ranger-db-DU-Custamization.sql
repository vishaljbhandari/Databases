DROP TABLE DUDUBAI_UNF_CONVERSION ;
DROP TABLE DUDUBAI_SS7_OPC_DPC ;
DROP TABLE DU_DUBAI_LOCAL_CALL_CHECK ;


CREATE TABLE DUDUBAI_UNF_CONVERSION
(
        ID               NUMBER(5)   PRIMARY KEY,
        DS_NAME          VARCHAR2(20),
        NUMBER_FORMAT       VARCHAR2(20),
        PREFIX_STRING    VARCHAR2(20),
        REPLACE_STRING   VARCHAR2(20)
);

CREATE TABLE DUDUBAI_SS7_OPC_DPC
(
        ID                              NUMBER(4),
        POINT_CODE              NUMBER(16) NOT NULL UNIQUE,
        CONSTRAINT PK_SS7_OPC_DPC PRIMARY KEY(ID)
);

CREATE TABLE DU_DUBAI_LOCAL_CALL_CHECK
(
        ID               NUMBER(5)   PRIMARY KEY,
        LOCAL_PREFIX     VARCHAR2(20) NOT NULL UNIQUE
);


