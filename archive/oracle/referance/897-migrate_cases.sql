INSERT INTO CASES(ID, NETWORK_ID, NAME, DESCRIPTION, OWNER_ID, CREATOR_ID, CREATED_DATE, MODIFIED_DATE, STATUS, REASON_ID, PENDING_TIME, ALARM_COUNT)
	SELECT C.ID, C.NETWORK_ID, C.NAME, C.DESCRIPTION, C.OWNER_ID, C.CREATOR_ID, C.CREATED_DATE,
	C.MODIFIED_DATE, C.STATUS, NCR.ID, C.PENDING_TIME, C.ALARM_COUNT
		FROM CASE@DU_NIKIRA C, CASE_REASON@DU_NIKIRA CR, CASE_REASONS NCR WHERE C.REASON_ID = CR.ID AND CR.REASON = NCR.REASON ;

